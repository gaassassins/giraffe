import UIKit


extension UIColor {
    
    class var giraffeLightColor: UIColor {
        return UIColor(red: 245/255.0, green: 211/255.0, blue: 163/255.0, alpha: 1.0)
    }
    
    class var giraffeDarkColor: UIColor {
        return UIColor(red: 250/255.0, green: 123/255.0, blue: 0/255.0, alpha: 1.0)
        
    }
    
    class var giraffeDeepColor: UIColor {
        return UIColor(red: 32/255.0, green: 59/255.0, blue: 104/255.0, alpha: 0.5)
        
    }
}
